#pragma once

// =============================================================================
// RuntimeRpcBinding -- non-template ErasedInvoker (FastLED #3246 / #3244 Tier 1A)
// =============================================================================
//
// Replaces per-signature `TypedRpcBinding<Sig>::invokeWithReturn` (~2.1 KB
// observed on LPC845 / AutoResearch) with a non-template invoke() body that
// dispatches through a tiny per-Sig trampoline plus a per-type ops table.
//
// Per-Sig cost goes from ~2 KB (full JsonArgConverter + TypeToJson +
// invokeImpl bodies emitted per signature) down to ~50-150 B (just the
// per-Sig trampoline). The shared dispatch lives in
// `runtime_rpc_binding.cpp.hpp`.
//
// Acceptance is in FastLED #3246: enabled on `!FL_PLATFORM_HAS_LARGE_MEMORY`
// targets via the compile-time fork in `Rpc::registerMethod`. Large-memory
// targets keep `TypedRpcBinding<Sig>` (faster -- no per-call heap, no
// indirect dispatch).

